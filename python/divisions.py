"""
Divisionsbeispiele
Demonstriert verschiedene Arten der Division in Python
"""

def main():
    """
    Zeigt Beispiele für verschiedene mathematische Operationen:
    - Ganzzahlige Division
    - Fließkommadivision
    - Potenzierung
    - Multiplikation
    """
    # Ausgabe der verschiedenen Divisionsarten
    print("Beispiele für verschiedene Divisionen:")
    print("1//2 =", 1//2, "(Ganzzahlige Division)")
    print("1/2 =", 1/2, "(Fließkommadivision)")
    print("2**3 =", 2**3, "(Potenzierung)")
    print("2*3 =", 2*3, "(Multiplikation)")

if __name__ == "__main__":
    main()
    